# ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡∏ä‡πà‡∏ß‡∏¢‡πÄ‡∏ä‡πá‡∏Ñ-‡∏ï‡∏¥‡∏î‡∏ï‡∏±‡πâ‡∏á ollama CLI ‡πÅ‡∏•‡∏∞‡∏î‡∏∂‡∏á‡πÇ‡∏°‡πÄ‡∏î‡∏• gemma3:latest ‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏™‡∏ï‡∏≤‡∏£‡πå‡∏ó‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏£‡∏±‡∏ô
def ensure_ollama_gemma():
    # 1) ‡∏ï‡∏¥‡∏î‡∏ï‡∏±‡πâ‡∏á ollama CLI ‡∏ñ‡πâ‡∏≤‡πÑ‡∏°‡πà‡∏°‡∏µ
    if shutil.which("ollama") is None:
        subprocess.run("curl -fsSL https://ollama.com/install.sh | sh", shell=True, check=True)
    # 2) ‡∏ï‡∏£‡∏ß‡∏à‡∏™‡∏≠‡∏ö‡∏ß‡πà‡∏≤‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏£‡∏±‡∏ô‡∏≠‡∏¢‡∏π‡πà (ollama list)
    proc = subprocess.run(["ollama", "list"], capture_output=True, text=True)
    if proc.returncode != 0 or "could not connect" in proc.stderr.lower():
        # ‡∏™‡∏ï‡∏≤‡∏£‡πå‡∏ó Ollama server ‡πÉ‡∏ô background
        subprocess.Popen([
            "ollama", "serve"
        ], stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)
        # ‡∏£‡∏≠‡∏™‡∏±‡∏Å‡∏Ñ‡∏£‡∏π‡πà‡πÉ‡∏´‡πâ‡πÄ‡∏ã‡∏¥‡∏£‡πå‡∏ü‡πÄ‡∏ß‡∏≠‡∏£‡πå‡∏û‡∏£‡πâ‡∏≠‡∏°‡∏£‡∏±‡∏ö‡∏Ñ‡∏≥‡∏™‡∏±‡πà‡∏á
        time.sleep(3)
    # 3) ‡∏î‡∏∂‡∏á‡πÇ‡∏°‡πÄ‡∏î‡∏• gemma3:latest ‡∏ñ‡πâ‡∏≤‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ
    result = subprocess.run(["ollama", "list"], capture_output=True, text=True)
    if "gemma3:latest" not in result.stdout:
        subprocess.run(["ollama", "pull", "gemma3:latest"], check=True)

